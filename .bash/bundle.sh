

# Version key/value should be on his own line
PACKAGE_VERSION=$(cat package.json \
  | grep version \
  | head -1 \
  | awk -F: '{ print $2 }' \
  | sed 's/[",]//g' \
  | tr -d '[[:space:]]')

PACKAGE_NAME=$(cat package.json \
  | grep name \
  | head -1 \
  | awk -F: '{ print $2 }' \
  | sed 's/[",]//g' \
  | tr -d '[[:space:]]')


# Delete bundle folder.
rm -rf ./bundle/$PACKAGE_VERSION
rm ./bundle/$PACKAGE_NAME-$PACKAGE_VERSION.zip

yarn makepot

# Bundle theme
grunt
